CREATE PROCEDURE `select_subnotes_recursive`(id_note integer unsigned)
BEGIN
WITH RECURSIVE cte_notes AS (
-- Passo base
(SELECT id_note_secondary, title, text
FROM subnote
LEFT JOIN note ON subnote.id_note_secondary = note.id
LEFT JOIN paragraph ON note.id = paragraph.id_note
WHERE id_note_principal = id_note
)
-- Passo Ricorsivo
UNION ALL
(SELECT s.id_note_secondary, n.title, p.text
FROM subnote s
LEFT JOIN note n ON s.id_note_secondary = n.id
LEFT JOIN paragraph p ON n.id = p.id_note
INNER JOIN cte_notes ON s.id_note_principal = cte_notes.id_note_secondary)
)
select * from cte_notes;
END ;;
